$NOSTANDARD ON
      SUBROUTINE ZEDATE(BDATE)
C   
C   Gets the data (DAY-MONTH-YEAR)
C   HP version
C
       CHARACTER*10 BDATE
       CHARACTER*9 ADATE
       CALL DATE(ADATE)
       WRITE(BDATE,100) ADATE
 100   FORMAT(A9,' ')
       RETURN
       END
